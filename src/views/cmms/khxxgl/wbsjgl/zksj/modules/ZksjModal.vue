        url: {
          add: "/wbsjgl/zksj/add",
          edit: "/wbsjgl/zksj/edit",
        },
      }
    },
    created () {
    },
    methods: {
      add () {
        this.edit({});
      },
      edit (record) {
        this.form.resetFields();
        this.model = Object.assign({}, record);
        this.visible = true;
        this.$nextTick(() => {
          this.form.setFieldsValue(pick(this.model,'khmc','zjhm','lxdh','lxdz','dw','wdmc','yhmc','yhkh','kzt','ybykjl','ysfxk','djxh','sbh','jsyj','sjje'))
		      //时间格式化
          this.form.setFieldsValue({yzfsrq:this.model.yzfsrq?moment(this.model.yzfsrq):null})
        });

      },
      close () {
        this.$emit('close');
        this.visible = false;
      },
      handleOk () {
        const that = this;
        // 触发表单验证
        this.form.validateFields((err, values) => {
          if (!err) {
            that.confirmLoading = true;
            let httpurl = '';
            let method = '';
            if(!this.model.id){
              httpurl+=this.url.add;
              method = 'post';
            }else{
              httpurl+=this.url.edit;
               method = 'put';
            }
            let formData = Object.assign(this.model, values);
            console.log(formData)

            //时间格式化
            formData.yzfsrq = formData.yzfsrq?formData.yzfsrq.format("YYYY-MM-DD"):null;
            httpAction(httpurl,formData,method).then((res)=>{
              if(res.success){
                that.$message.success(res.message);
                that.$emit('ok');
              }else{
                that.$message.warning(res.message);
              }
            }).finally(() => {
              that.confirmLoading = false;
              that.close();
            })



          }
        })
      },
      handleCancel () {
        this.close()
      },


    }
  }
</script>

