          :wrapperCol="wrapperCol"
          label="定期最大存期">
          <a-input placeholder="请输入定期最大存期" v-decorator="['dqzdcq', {}]" />
        </a-form-item>
        <a-form-item
          :labelCol="labelCol"
          :wrapperCol="wrapperCol"
          label="开户日期">
          <a-input placeholder="请输入开户日期" v-decorator="['khrq', {}]" />
