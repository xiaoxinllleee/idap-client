        },

        confirmLoading: false,
        form: this.$form.createForm(this),
        validatorRules:{
        acctNo:{rules: [{ required: true, message: '请输入账号!' }]},
        brNo:{rules: [{ required: true, message: '请输入交易机构!' }]},
        recNo:{rules: [{ required: true, message: '请输入记录号!' }]},
        dtnum:{rules: [{ required: true, message: '请输入天入库表编号-对不同的表名唯一!' }]},
        },
        url: {
          add: "/nbzjykb/nbzjykb/add",
          edit: "/nbzjykb/nbzjykb/edit",
        },
      }
    },
    created () {
    },
    methods: {
      add () {
        this.edit({});
      },
      edit (record) {
        this.form.resetFields();
        this.model = Object.assign({}, record);
        this.visible = true;
        this.$nextTick(() => {
          this.form.setFieldsValue(pick(this.model,'dataDate','loadDate','acctNo','acctName','tranType','stat','postDate','trnDate','trnCode','trnName','cashTots','channel','brNo','brName','brTell','brTellName','srnInfo','accountno','amount','currentBalance','comments','jrnlNo','currency','trnTime','correction','empType','terminalId','recNo','legalNo','dtnum'))
		  //时间格式化
          this.form.setFieldsValue({dttime:this.model.dttime?moment(this.model.dttime):null})
        });

      },
      close () {
        this.$emit('close');
        this.visible = false;
      },
      handleOk () {
        const that = this;
        // 触发表单验证
        this.form.validateFields((err, values) => {
          if (!err) {
            that.confirmLoading = true;
            let httpurl = '';
            let method = '';
            if(!this.model.id){
              httpurl+=this.url.add;
              method = 'post';
            }else{
              httpurl+=this.url.edit;
               method = 'put';
            }
            let formData = Object.assign(this.model, values);
            //时间格式化
            formData.dttime = formData.dttime?formData.dttime.format():null;
            
            console.log(formData)
            httpAction(httpurl,formData,method).then((res)=>{
              if(res.success){
                that.$message.success(res.message);
                that.$emit('ok');
              }else{
                that.$message.warning(res.message);
              }
            }).finally(() => {
              that.confirmLoading = false;
              that.close();
            })



          }
        })
      },
      handleCancel () {
        this.close()
      },


    }
  }
</script>

