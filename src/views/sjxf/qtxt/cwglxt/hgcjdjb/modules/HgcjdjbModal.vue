        },

        confirmLoading: false,
        form: this.$form.createForm(this),
        validatorRules:{
        },
        url: {
          add: "/hgcjdjb/hgcjdjb/add",
          edit: "/hgcjdjb/hgcjdjb/edit",
        },
      }
    },
    created () {
    },
    methods: {
      add () {
        this.edit({});
      },
      edit (record) {
        this.form.resetFields();
        this.model = Object.assign({}, record);
        this.visible = true;
        this.$nextTick(() => {
          this.form.setFieldsValue(pick(this.model,'pactNo','pledgeType','txCnt','pledgeCnt','tradeType','acNo','acId','repRate','pledgeBal','firstAmt','isIntst','dateRateNum','intstAmt','mtrAmt','firstSetrType','mtrSetrType','signDate','firstDate','mrtDate','dCnt','realDays','regDate','lJtDate','tradeNo','ctpyNo','version','appNo','brNo','lvlFive','depNo','suppNo','sts','ljJtIntst','ljSqIntst','prdtNo','ctpyTrader','trader','ywNo','planNo','chnlInd','upBrNo','brf','curNo','bctpyNo','bversion','sDate','eDate','loadDate','legalNo'))
		  //时间格式化
        });

      },
      close () {
        this.$emit('close');
        this.visible = false;
      },
      handleOk () {
        const that = this;
        // 触发表单验证
        this.form.validateFields((err, values) => {
          if (!err) {
            that.confirmLoading = true;
            let httpurl = '';
            let method = '';
            if(!this.model.id){
              httpurl+=this.url.add;
              method = 'post';
            }else{
              httpurl+=this.url.edit;
               method = 'put';
            }
            let formData = Object.assign(this.model, values);
            //时间格式化
            
            console.log(formData)
            httpAction(httpurl,formData,method).then((res)=>{
              if(res.success){
                that.$message.success(res.message);
                that.$emit('ok');
              }else{
                that.$message.warning(res.message);
              }
            }).finally(() => {
              that.confirmLoading = false;
              that.close();
            })



          }
        })
      },
      handleCancel () {
        this.close()
      },


    }
  }
</script>

