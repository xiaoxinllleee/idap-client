        },

        confirmLoading: false,
        form: this.$form.createForm(this),
        validatorRules:{
        dtnum:{rules: [{ required: true, message: '请输入天入库表编号-对不同的表名唯一!' }]},
        },
        url: {
          add: "/yddsfdsjyls/yddsfdsjyls/add",
          edit: "/yddsfdsjyls/yddsfdsjyls/edit",
        },
      }
    },
    created () {
    },
    methods: {
      add () {
        this.edit({});
      },
      edit (record) {
        this.form.resetFields();
        this.model = Object.assign({}, record);
        this.visible = true;
        this.$nextTick(() => {
          this.form.setFieldsValue(pick(this.model,'workdate','serialno','acctDate','hostserialno','transno','reqdatetime','tradetype','tellerno','bankno','needchk','hoststatus','hostseqno','hostdate','hosterrcode','hosterrmsg','hostchkflag','corpstatus','corpseqno','corpdate','corperrcode','corperrmsg','corpchkflag','busistatus','busierrcode','busierrmsg','payeraccno','payername','acctseqno','payeeaccno','payeename','payway','cardflag','amt','revflag','username','useraddr','useridentno','tvcardno','gameid','gamename','origtrandate','orighostno','batchflag','batchtransno','batchxh','chnlseqno','chnldate','payeracctseqno','payeeacctseqno','controlflag','uuid','zjywlx','dataDate','loadDate','legalNo','dtnum'))
		  //时间格式化
          this.form.setFieldsValue({dttime:this.model.dttime?moment(this.model.dttime):null})
        });

      },
      close () {
        this.$emit('close');
        this.visible = false;
      },
      handleOk () {
        const that = this;
        // 触发表单验证
        this.form.validateFields((err, values) => {
          if (!err) {
            that.confirmLoading = true;
            let httpurl = '';
            let method = '';
            if(!this.model.id){
              httpurl+=this.url.add;
              method = 'post';
            }else{
              httpurl+=this.url.edit;
               method = 'put';
            }
            let formData = Object.assign(this.model, values);
            //时间格式化
            formData.dttime = formData.dttime?formData.dttime.format():null;
            
            console.log(formData)
            httpAction(httpurl,formData,method).then((res)=>{
              if(res.success){
                that.$message.success(res.message);
                that.$emit('ok');
              }else{
                that.$message.warning(res.message);
              }
            }).finally(() => {
              that.confirmLoading = false;
              that.close();
            })



          }
        })
      },
      handleCancel () {
        this.close()
      },


    }
  }
</script>

