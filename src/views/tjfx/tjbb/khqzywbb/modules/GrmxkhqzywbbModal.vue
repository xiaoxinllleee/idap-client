    methods: {
      add () {
        this.edit({});
      },
      edit (record) {
        this.form.resetFields();
        this.model = Object.assign({}, record);
        this.visible = true;
        this.$nextTick(() => {
          this.form.setFieldsValue(pick(this.model,'ssyxdy','sszh','xz','xzc','khlx','hhbm','khmc','zjhm','sfhz','zkhjl','kcqzyw','sfqzdk','sfqzck','sfqzwsyh','sfqzsjyh','sfqzetc','sfqzezf','sfqzejf','sfqzpos','sfqzznzd','sfqzlc','sfqzbx'))
