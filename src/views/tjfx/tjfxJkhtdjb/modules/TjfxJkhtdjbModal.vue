    },
    created () {
    },
    methods: {
      add () {
        this.edit({});
      },
      edit (record) {
        this.form.resetFields();
        this.model = Object.assign({}, record);
        this.visible = true;
        this.$nextTick(() => {
          this.form.setFieldsValue(pick(this.model,'khmc','zjhm','khh','ywpz','htbh','htlx','htje','ysyed','kyed','htqsr','htdqr','qyrq','zzrq','hkzq','hkfs','qyll','dbfs','dyzybzhtbh','xqzhmt','jkyy','khjl'))
